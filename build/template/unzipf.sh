#!/bin/bash

# Проверка наличия аргумента
if [ -z "$1" ]; then
  echo "Использование: $0 <файл.docx>"
  exit 1
fi

# Получение имени файла без расширения
file_name=$(basename "$1" .docx)

# Проверка существования файла
if [ ! -f "$1" ]; then
  echo "Ошибка: Файл '$1' не найден."
  exit 1
fi

# Создание директории с именем файла
mkdir -p "$file_name"

# Переход в созданную директорию
cd "$file_name"

# Распаковка файла
unzip "../$1"

# Возврат в исходную директорию
cd ..

echo "Файл '$1' распакован в директорию '$file_name'."

exit 0

